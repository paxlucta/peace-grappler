def _load_dotenv():
    """Load .env files into os.environ so subprocesses (gemini, codex, etc.)
    inherit keys like GEMINI_API_KEY regardless of which directory the app
    was launched from. Searches in this order, first match wins per key:

        1. <project_root>/.env         ← typical "I just dropped a .env here"
        2. <project_root>/.gemini/.env ← matches the gemini CLI's own lookup
        3. ~/.gemini/.env              ← user-home location

    Existing os.environ values are NOT overwritten so an explicit shell
    export still wins over a stale .env entry.
    """
    project_root = Path(__file__).parent.parent
    candidates = [
        project_root / ".env",
        project_root / ".gemini" / ".env",
        Path.home() / ".gemini" / ".env",
    ]
    for path in candidates:
        if not path.is_file():
            continue
        try:
            for line in path.read_text().splitlines():
                line = line.strip()
                if not line or line.startswith("#") or "=" not in line:
                    continue
                if line.startswith("export "):
                    line = line[7:].lstrip()
                key, _, val = line.partition("=")
                key = key.strip()
                val = val.strip()
                # Strip surrounding single/double quotes if balanced.
                if (len(val) >= 2 and val[0] == val[-1]
                        and val[0] in ("'", '"')):
                    val = val[1:-1]
                if key and key not in os.environ:
                    os.environ[key] = val
        except Exception as exc:
            print(f"[env] Couldn't read {path}: {exc}")


_load_dotenv()


