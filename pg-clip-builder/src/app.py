def _compute_build_info():
    """Return a dict describing the current build: a numeric version
    derived from the git commit count, the short SHA, a "dirty" flag if
    the working tree has uncommitted changes, and the timestamp of the
    most recently modified ``.py`` file (so dirty builds also visibly
    advance after every code change without needing a commit)."""
    info = {"version": "0.0.0", "sha": "", "dirty": False, "modified": ""}
    try:
        count = subprocess.run(
            ["git", "rev-list", "--count", "HEAD"],
            capture_output=True, text=True, cwd=str(ROOT_DIR), timeout=4,
        ).stdout.strip()
        sha = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            capture_output=True, text=True, cwd=str(ROOT_DIR), timeout=4,
        ).stdout.strip()
        # `git status --porcelain` lists changed paths; non-empty → dirty.
        dirty = bool(subprocess.run(
            ["git", "status", "--porcelain"],
            capture_output=True, text=True, cwd=str(ROOT_DIR), timeout=4,
        ).stdout.strip())
        if count.isdigit():
            n = int(count)
            # Semver-ish: every 100 commits bumps the minor.
            major, rem = divmod(n, 10000)
            minor, patch = divmod(rem, 100)
            info["version"] = f"{major}.{minor}.{patch}"
        info["sha"] = sha
        info["dirty"] = dirty
    except Exception:
        pass
    # Track the most-recent .py mtime — dirty edits without a commit
    # still advance this so the modal shows the user that their change
    # is reflected in the running server.
    try:
        latest = 0.0
        for p in (ROOT_DIR / "src").glob("*.py"):
            try:
                latest = max(latest, p.stat().st_mtime)
            except OSError:
                pass
        if latest:
            from datetime import datetime as _dt
            info["modified"] = _dt.fromtimestamp(latest).strftime("%Y-%m-%d %H:%M:%S")
    except Exception:
        pass
    return info


