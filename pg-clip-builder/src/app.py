REPO_HTTPS = "https://github.com/paxlucta/peace-grappler.git"


@app.route("/api/update", methods=["POST"])
def check_for_updates():
    """Pull latest code from git. Werkzeug reloader restarts if .py files changed."""
    try:
        # Find the git repo root (may be parent of pg-clip-builder)
        git_root = subprocess.run(
            ["git", "rev-parse", "--show-toplevel"],
            capture_output=True, text=True, cwd=str(ROOT_DIR), timeout=10,
        ).stdout.strip()

        if not git_root:
            # Not a git repo — bootstrap one so updates work going forward
            git_root = str(ROOT_DIR.parent)  # peace-grappler/ level
            subprocess.run(
                ["git", "init"], cwd=git_root,
                capture_output=True, timeout=10,
            )
            subprocess.run(
                ["git", "remote", "add", "origin", REPO_HTTPS],
                cwd=git_root, capture_output=True, timeout=10,
            )
            subprocess.run(
                ["git", "fetch", "origin"],
                cwd=git_root, capture_output=True, timeout=30,
            )
            # Set the current state as tracking main
            subprocess.run(
                ["git", "checkout", "-b", "main"],
                cwd=git_root, capture_output=True, timeout=10,
            )
            subprocess.run(
                ["git", "branch", "--set-upstream-to=origin/main", "main"],
                cwd=git_root, capture_output=True, timeout=10,
            )
            # Reset to match remote (picks up latest code)
            result = subprocess.run(
                ["git", "reset", "--hard", "origin/main"],
                cwd=git_root, capture_output=True, text=True, timeout=30,
            )
            if result.returncode != 0:
                return jsonify({"status": "error",
                                "message": "Bootstrap failed: " + result.stderr.strip()})
            return jsonify({"status": "updated", "from": "initial", "to": "latest"})

        # Ensure remote uses HTTPS (SSH won't work without keys)
        cur_remote = subprocess.run(
            ["git", "config", "--get", "remote.origin.url"],
            capture_output=True, text=True, cwd=git_root, timeout=10,
        ).stdout.strip()
        if cur_remote.startswith("git@"):
            subprocess.run(
                ["git", "remote", "set-url", "origin", REPO_HTTPS],
                cwd=git_root, capture_output=True, timeout=10,
            )

        # Fetch latest
        fetch = subprocess.run(
            ["git", "fetch", "origin"],
            capture_output=True, text=True, cwd=git_root, timeout=30,
        )
        if fetch.returncode != 0:
            return jsonify({"status": "error",
                            "message": "Fetch failed: " + fetch.stderr.strip()})

        # Check if there are updates
        local = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True, text=True, cwd=git_root, timeout=10,
        ).stdout.strip()

        remote = subprocess.run(
            ["git", "rev-parse", "@{u}"],
            capture_output=True, text=True, cwd=git_root, timeout=10,
        ).stdout.strip()

        if local == remote:
            return jsonify({"status": "up-to-date"})

        # Pull (reset to match remote — safe because user code is in videos/assets/output)
        result = subprocess.run(
            ["git", "reset", "--hard", "@{u}"],
            capture_output=True, text=True, cwd=git_root, timeout=30,
        )

        if result.returncode != 0:
            return jsonify({"status": "error",
                            "message": "Pull failed: " + result.stderr.strip()})

        # Reinstall deps in case requirements.txt changed
        venv_pip = ROOT_DIR / ".venv" / "bin" / "pip"
        req_file = ROOT_DIR / "requirements.txt"
        if venv_pip.exists() and req_file.exists():
            subprocess.Popen(
                [str(venv_pip), "install", "-q", "-r", str(req_file)],
                cwd=str(ROOT_DIR),
            )

        return jsonify({"status": "updated",
                        "from": local[:8], "to": remote[:8]})

    except Exception as e:
        return jsonify({"status": "error", "message": str(e)})


