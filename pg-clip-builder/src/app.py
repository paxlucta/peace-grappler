def _start_video_watcher():
    """Background thread that watches the active profile's source folder
    for new files, registers them, and auto-analyzes unanalyzed videos.

    The source folder is re-resolved from app_config every tick so a profile
    switch in /settings is honored without a restart. Each profile keeps a
    private set of "known files" so switching back doesn't re-register
    everything as new.
    """
    import threading
    from video import VIDEO_EXTENSIONS
    from db import register_video, get_all_videos, get_analyzed_tags

    SCAN_INTERVAL = 30  # seconds between local scans
    DRIVE_PULL_INTERVAL = 300  # seconds between Drive pulls (every 5 minutes)
    _known_files_by_profile = {}   # profile_name -> set[str path]
    _last_drive_pull = [0.0]       # mutable so the inner func can update it

    def _current_paths():
        """Return (active_profile_name, source_path) freshly resolved."""
        import app_config
        return (
            app_config.get_active_profile_name(),
            app_config.get_source_dir(),
        )

    def _maybe_pull_drive(video_dir):
        """Pull from the Drive inbox folder if connected and configured."""
        if not features.DRIVE_ENABLED:
            return
        import time as _t
        if _t.time() - _last_drive_pull[0] < DRIVE_PULL_INTERVAL:
            return
        try:
            import drive
            from db import register_video as _rv
            from db import get_video_by_drive_file_id, set_video_drive_info
            cfg = drive.get_config()
            if not (cfg["app_configured"] and cfg["has_token"]
                    and cfg["inbox_folder_id"]):
                return
            _last_drive_pull[0] = _t.time()
            items = drive.list_inbox_videos()
            existing_local = {p.name for p in video_dir.iterdir() if p.is_file()}
            for it in items:
                fid, name = it["id"], it["name"]
                if get_video_by_drive_file_id(fid) or name in existing_local:
                    continue
                dest = video_dir / name
                print(f"[watcher] Drive pull: downloading {name}...")
                try:
                    drive.download_file(fid, dest)
                    vid_id = _rv(dest)
                    set_video_drive_info(
                        vid_id, fid,
                        f"https://drive.google.com/file/d/{fid}/view",
                    )
                    print(f"[watcher] Drive pull: registered {name}")
                except Exception as exc:
                    print(f"[watcher] Drive pull failed for {name}: {exc}")
        except Exception as exc:
            print(f"[watcher] Drive pull error: {exc}")

    def _scan_and_analyze():
        profile, video_dir = _current_paths()
        _maybe_pull_drive(video_dir)
        video_dir.mkdir(parents=True, exist_ok=True)
        known = _known_files_by_profile.setdefault(profile, set())
        current_files = set()
        for root, _, files in os.walk(video_dir):
            for name in sorted(files):
                if Path(name).suffix.lower() in VIDEO_EXTENSIONS and not name.startswith("."):
                    current_files.add(str(Path(root) / name))

        new_files = current_files - known
        known.update(current_files)

        if new_files:
            print(f"[watcher] Found {len(new_files)} new video(s), registering...")
            for fpath in sorted(new_files):
                try:
                    register_video(fpath)
                    print(f"[watcher] Registered: {Path(fpath).name}")
                except Exception as exc:
                    print(f"[watcher] Failed to register {fpath}: {exc}")

        # Auto-analyze unanalyzed videos
        try:
            from analyzer import analyze_full, save_analysis, ALL_TAG_SET, get_analyzed_tags as get_at
            videos = get_all_videos()
            for v in videos:
                analyzed_tags = get_at(v["id"])
                if not analyzed_tags and v["duration"] > 0:
                    print(f"[watcher] Auto-analyzing: {v['filename']}...")
                    try:
                        result = analyze_full(v["path"], v["duration"])
                        if result:
                            save_analysis(v["id"], result["tags"],
                                          result["moments"], list(ALL_TAG_SET))
                            print(f"[watcher] Analyzed {v['filename']}: {len(result['tags'])} tags")
                        else:
                            print(f"[watcher] Analysis returned no results for {v['filename']}")
                    except Exception as exc:
                        print(f"[watcher] Analysis failed for {v['filename']}: {exc}")
        except Exception as exc:
            print(f"[watcher] Auto-analyze error: {exc}")

    def _watcher_loop():
        # Initial scan: seed the known-files set for the active profile so
        # we don't re-register pre-existing videos as "new".
        profile, video_dir = _current_paths()
        video_dir.mkdir(parents=True, exist_ok=True)
        known = _known_files_by_profile.setdefault(profile, set())
        for root, _, files in os.walk(video_dir):
            for name in sorted(files):
                if (Path(name).suffix.lower() in VIDEO_EXTENSIONS
                        and not name.startswith(".")):
                    known.add(str(Path(root) / name))
        print(f"[watcher] Watching {video_dir} for profile '{profile}' "
              f"({len(known)} existing videos)")

        import time as _time
        while True:
            _time.sleep(SCAN_INTERVAL)
            try:
                _scan_and_analyze()
            except Exception as exc:
                print(f"[watcher] Error: {exc}")

    t = threading.Thread(target=_watcher_loop, daemon=True)
    t.start()


def _wait_for_port_free(port, timeout=15.0):
    """Poll until *port* is bindable on 0.0.0.0 or *timeout* elapses.

    Used when this process was spawned by ``/settings/api/restart`` —
    the old instance is still alive and holding the port until its
    SIGTERM lands. We try a throwaway bind every 300 ms so app.run()
    only gets called once the kernel has released the address.
    """
    import socket as _sock
    import time as _t
    deadline = _t.time() + max(0.5, float(timeout))
    while _t.time() < deadline:
        s = _sock.socket(_sock.AF_INET, _sock.SOCK_STREAM)
        try:
            s.setsockopt(_sock.SOL_SOCKET, _sock.SO_REUSEADDR, 1)
            s.bind(("0.0.0.0", port))
            s.close()
            return True
        except OSError:
            try: s.close()
            except Exception: pass
            _t.sleep(0.3)
    return False


def main():
    port = 5555
    if "--port" in sys.argv:
        idx = sys.argv.index("--port")
        if idx + 1 < len(sys.argv):
            port = int(sys.argv[idx + 1])

